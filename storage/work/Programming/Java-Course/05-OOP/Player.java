public class Player {
  private int health = 100;
  private int damage = 90;
  
  public void setHealth(int a) {
    health = a;
  }
  
  public int getHealth() {
    return health;
  }
  
  public void setDamage(int a) {
    damage = a;
  }
  public int getDamage() {
    return damage;
  }
}